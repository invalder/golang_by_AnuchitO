package main

import "fmt"

func main() {
	fmt.Println("Hello Gopher!!!")

	fmt.Printf("hello %s!!!!\n", "Gopher")
	fmt.Println("New Line")
	fmt.Printf("hello %d\n", 66)
	fmt.Printf("hello %v\n", "Gopher")
	fmt.Printf("hello %v\n", 77)

	fmt.Printf("hello %v : %v!!!!\n", "ฮัลโหลสวัสดี", 21474836482313446546546546546546)
}
